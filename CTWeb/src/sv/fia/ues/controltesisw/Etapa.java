package sv.fia.ues.controltesisw;

public class Etapa {
	private String idEtapa;
	private String fechaDeInicioEtapa;
	private String fechaDeFinalizacionEtapa;

	public Etapa(){
		
	}
	public String getIdEtapa() {
		return idEtapa;
	}
	public void setIdEtapa(String idEtapa) {
		this.idEtapa = idEtapa;
	}
	public String getFechaDeInicioEtapa() {
		return fechaDeInicioEtapa;
	}
	public void setFechaDeInicioEtapa(String fechaDeInicioEtapa) {
		this.fechaDeInicioEtapa = fechaDeInicioEtapa;
	}
	public String getFechaDeFinalizacionEtapa() {
		return fechaDeFinalizacionEtapa;
	}
	public void setFechaDeFinalizacionEtapa(String fechaDeFinalizacionEtapa) {
		this.fechaDeFinalizacionEtapa = fechaDeFinalizacionEtapa;
	}



}
